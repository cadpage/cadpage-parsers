  private static final Pattern AUTO_NOTIFICATION_PTN = Pattern.compile("(?:CAD:(\\d+-\\d+) )?AUTO NOTIFI?CATION(?: ONLY OF)?: *([-A-Z0-9]+) +DISPATCHED AT[: ]*(.*?)(?:, *([A-Za-z]+))?(?: (?:LAT|Lat): +(\\d*) +(?:LONG|Long): *(\\d*))?");

  @Override
  protected boolean parseMsg(String body, Data data) {

    // Parse special auto-notification alert
    Matcher match = AUTO_NOTIFICATION_PTN.matcher(body);
    if (match.matches()) {
      setFieldList("ID CODE CALL ADDR APT CITY GPS");
      data.strCallId = getOptGroup(match.group(1));
      data.strCode = match.group(2);
      String call = lookupCallCode(data.strCode);
      if (call == null) call = data.strCode;
      data.strCall = call;
      parseAddress(match.group(3).trim(), data);
      String city = match.group(4);
      if (city != null) {
        String tmp = CITY_CODES.getCodeDescription(city.toUpperCase(), true);
        if (tmp != null) city = tmp;
        data.strCity = city;
      }
      setGPSLoc(convGPS(match.group(4))+','+convGPS(match.group(5)), data);
      return true;
    }

    body = stripFieldStart(body, "CONFIRM RESPONSE");
    body = body.replace(">XST:", ">X ST:");
    body = body.replace(" COMMENTS:", ">COMMENTS:");
    if (parseFields(body.split(">"), data)) {
      if (data.strPlace.equals("WESTERN ARIZONA REGIONAL") ||
          data.strAddress.equalsIgnoreCase("2735 Silver Creek Rd")) {
        data.strCity = "BULLHEAD CITY";
        data.strState = "AZ";
      }
      else if (data.strPlace.equals("VALLEY VIEW MEDICAL CENTER") ||
               data.strPlace.equals("VALLEY VIEW")) {
        data.strCity = "FORT MOJAVE";
        data.strState = "AZ";
      }
      return true;
    }

    // No go for regular dispatch page
    // If positive confirmation, this might be a general alert
    if (!isPositiveId()) return false;

    // If body starts with something that looks like a unit
    // parse the unit field and report result as general alert
    match = GEN_ALT_UNIT_PTN.matcher(body);
    if (!match.find()) return false;
    data.initialize(this);
    setFieldList("CALL UNIT INFO");
    data.msgType = MsgType.GEN_ALERT;
    data.strUnit = match.group(1);
    data.strSupp = body;
    return true;
  }

  private String convGPS(String coord) {
    if (coord == null) return "";
    int pt = coord.length()-6;
    if (pt < 2) return "";
    return coord.substring(0,pt) + '.' + coord.substring(pt);
  }

  @Override
  public Field getField(String name) {
    if (name.equals("CALL")) return new MyCallField();
    if (name.equals("NUMBER")) return new AddressField("\\d+", true);
    if (name.equals("ADDR")) return new MyAddressField();
    if (name.equals("X")) return new MyCrossField();
    if (name.equals("CITY_EXT")) return new MyCityExtField();
    if (name.equals("MAP")) return new MyMapField();
    if (name.equals("INFO")) return new MyInfoField();
    if (name.equals("GPS")) return new MyGPSField();
    return super.getField(name);
  }

  private static final Pattern ID_CALL_PTN = Pattern.compile("CAD:(\\d+-\\d+) *(.*)");
  private static final Pattern CODE_CALL_PTN = Pattern.compile("(.*?)(?: *- +| +- *)(.*)");
  private class MyCallField extends CallField {

    @Override
    public void parse(String field, Data data) {

      Matcher match = ID_CALL_PTN.matcher(field);
      if (match.matches()) {
        data.strCallId = match.group(1);
        field = match.group(2);
      }

      field = stripFieldEnd(field, "-");
      String code;
      match = CODE_CALL_PTN.matcher(field);
      if (match.matches()) {
        code = match.group(1);
        field = match.group(2);
      } else {
        code = field;
        field = "";
      }
      String desc = lookupCallCode(code);
      if (desc != null) {
        if (desc.startsWith(field)) {
          field = desc;
        } else {
          field = append(field, " - ", desc);
        }
      } else if (field.length() == 0) {
        field = code;
      }
      data.strCode = code;
      data.strCall = field;
    }

    @Override
    public String getFieldNames() {
      return "ID CODE CALL";
    }
  }

  private static final Pattern BLANK_DASH_PTN = Pattern.compile("(.*) -(?:BLD/APT:)? *(.*)");
  private class MyAddressField extends AddressField {

    @Override
    public void parse(String field, Data data) {

      field = parseCity(field, data);

      field = stripFieldEnd(field, "-");
      field = stripFieldEnd(field, "/");
      Matcher match = BLANK_DASH_PTN.matcher(field);
      if (match.matches()) {
        String tmp = match.group(1).trim();
        if (!NUMERIC.matcher(tmp).matches()) {
          field = tmp;
          data.strApt = stripFieldStart(match.group(2).trim(), "/");
        }
      }

      field = append(data.strAddress, " - ", field);
      data.strAddress = "";
      parseAddress(field, data);
    }

    @Override
    public String getFieldNames() {
      return "ADDR APT CITY ST";
    }
  }

  private class MyCrossField extends CrossField {
    @Override
    public void parse(String field, Data data) {
      field = field.replace("No X-Street", "");
      field = stripFieldStart(field, "/");
      field = stripFieldEnd(field, "/");
      super.parse(field, data);
    }
  }

